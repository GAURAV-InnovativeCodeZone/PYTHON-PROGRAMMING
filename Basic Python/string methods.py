''' Different Types of String Function '''

intro = "my name is GAURAV PANWAR."
print(intro)

print(len(intro))    #length of string
print(intro.endswith("PANWAR."))   #ending 
print(intro.count("is"))    # counting of word in string
print(intro.upper())  #all letter capital
print(intro.lower())   #all letter small 
print(intro.find("is"))     #indexing of word
print(intro.capitalize())     #first charater capital
print(intro.replace("name is","self"))  #replace old word to new word 
