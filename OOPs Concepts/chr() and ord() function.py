# chr() function

print('chr() function_________')
n = chr(65)  # Return ASCII capital alphabet(A-Z)
print(type(n), n)

n = chr(97)  # Return ASCII small alphabet(a-z)
print(type(n), n)


# ord() function

print('ord() function_________')
m = ord('A')  # Return ASCII value of capital alphabet(65-90)
print(type(m), m)

m = ord('a')
print(type(m), m)  # Return ASCII value of small alphabet(97-122)
